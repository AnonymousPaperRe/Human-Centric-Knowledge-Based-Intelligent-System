def _traverse_generic_depends_on(ca_id: str, spec_id: str, report: dict):
    """Generic fallback: traverse DEPENDS_ON up to 2 hops."""
    direct = run_cypher(
        "MATCH (dep:Specification)-[:DEPENDS_ON]->(s:Specification {identifier: $sid}) "
        "RETURN dep.identifier AS id, labels(dep) AS lbls",
        {"sid": spec_id},
    )
    for row in direct:
        did = row.get("id")
        if did:
            report["affected_specs"].append({"identifier": did, "severity": "HIGH"})
            _impact_spec_generic(ca_id, did, "HIGH", "DIRECT")

    indirect = run_cypher(
        "MATCH (dep:Specification)-[:DEPENDS_ON*2]->(s:Specification {identifier: $sid}) "
        "RETURN dep.identifier AS id",
        {"sid": spec_id},
    )
    for row in indirect:
        did = row.get("id")
        if did:
            report["affected_specs"].append({"identifier": did, "severity": "MEDIUM"})
            _impact_spec_generic(ca_id, did, "MEDIUM", "INDIRECT")


