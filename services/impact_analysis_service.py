"""
Impact Analysis Service — per-spec-type KG traversal that creates
IMPACTS_* relationships (aligned with newEventLayer.py reference) and
returns a categorised impact report.

Relationship types match the reference exactly:
  Spec-layer:      IMPACTS_WORKSTEP_SPEC, IMPACTS_PART_SPEC,
                   IMPACTS_EQUIPMENT_SPEC, IMPACTS_OPERATION_SPEC,
                   IMPACTS_SPECIFICATION (generic fallback)
  Execution-layer: IMPACTS_PROCESS, IMPACTS_TASK,
                   IMPACTS_ORDER, IMPACTS_INVENTORY,
                   IMPACTS_VEHICLE, IMPACTS_PART_INSTANCE,
                   IMPACTS_EQUIPMENT_INSTANCE

Signature:
    run(change_action_id, spec_identifier, spec_type) -> dict
"""
from datetime import datetime
from services.neo4j_service import run_cypher


def _now_str() -> str:
    return datetime.now().strftime("%Y-%m-%d %H:%M:%S")


# ── Relationship creators ─────────────────────────────────────────────────────

def _impact_rel(ca_id: str, node_id: str, node_label: str, rel_type: str,
                severity: str, impact_type: str = "DIRECT", hold: bool = False):
    """Create an IMPACTS_* relationship on the ChangeAction."""
    run_cypher(
        f"MATCH (ca:ChangeAction {{identifier: $ca_id}}), "
        f"(n:{node_label} {{identifier: $node_id}}) "
        f"MERGE (ca)-[r:{rel_type}]->(n) "
        f"SET r.severity = $severity, r.impactType = $impact_type, "
        f"r.holdRequired = $hold, r.resolved = false, "
        f"r.reason = 'auto-generated by impact analysis'",
        {
            "ca_id": ca_id, "node_id": node_id,
            "severity": severity, "impact_type": impact_type, "hold": hold,
        },
    )


def _impact_spec_generic(ca_id, node_id, severity, impact_type="DIRECT"):
    _impact_rel(ca_id, node_id, "Specification", "IMPACTS_SPECIFICATION",
                severity, impact_type)


def _impact_workstep(ca_id, node_id, severity="HIGH"):
    _impact_rel(ca_id, node_id, "WorkStepSpec", "IMPACTS_WORKSTEP_SPEC", severity)


def _impact_part(ca_id, node_id, severity="HIGH"):
    _impact_rel(ca_id, node_id, "PartSpec", "IMPACTS_PART_SPEC", severity)


def _impact_equip(ca_id, node_id, severity="MEDIUM"):
    _impact_rel(ca_id, node_id, "EquipmentSpec", "IMPACTS_EQUIPMENT_SPEC", severity)


def _impact_operation(ca_id, node_id, severity="HIGH"):
    _impact_rel(ca_id, node_id, "OperationSpec", "IMPACTS_OPERATION_SPEC", severity)


def _impact_process(ca_id, node_id, severity="HIGH", hold=False):
    _impact_rel(ca_id, node_id, "ProductionProcess", "IMPACTS_PROCESS",
                severity, hold=hold)


def _impact_task(ca_id, node_id, severity="HIGH"):
    _impact_rel(ca_id, node_id, "OperationalTask", "IMPACTS_TASK", severity)


def _impact_order(ca_id, node_id, severity="HIGH", hold=True):
    _impact_rel(ca_id, node_id, "ProductionOrder", "IMPACTS_ORDER",
                severity, hold=hold)


def _impact_inventory(ca_id, node_id, severity="MEDIUM"):
    _impact_rel(ca_id, node_id, "InventoryRecord", "IMPACTS_INVENTORY", severity)


def _impact_vehicle(ca_id, node_id, severity="MEDIUM"):
    _impact_rel(ca_id, node_id, "Vehicle", "IMPACTS_VEHICLE", severity)


# ── Per-type traversal helpers ────────────────────────────────────────────────

def _traverse_part_spec(ca_id: str, spec_id: str, report: dict):
    """PartSpec → operations, inventory, production orders, processes."""
    # Operations requiring this part
    ops = run_cypher(
        "MATCH (op:OperationSpec)-[:REQUIRES_PART|requiresPart]->"
        "(s:Specification {identifier: $sid}) "
        "RETURN DISTINCT op.identifier AS id",
        {"sid": spec_id},
    )
    for row in ops:
        oid = row.get("id")
        if oid:
            report["affected_operations"].append({"identifier": oid, "severity": "HIGH"})
            _impact_operation(ca_id, oid, "HIGH")

    # Inventory records
    inv = run_cypher(
        "MATCH (ir:InventoryRecord)-[:tracksSpec|TRACKS_SPEC|tracksPartSpec]->"
        "(s:Specification {identifier: $sid}) "
        "RETURN DISTINCT ir.identifier AS id",
        {"sid": spec_id},
    )
    for row in inv:
        iid = row.get("id")
        if iid:
            report["affected_inventory"].append({"identifier": iid, "severity": "MEDIUM"})
            _impact_inventory(ca_id, iid, "MEDIUM")

    # Production orders that use this part
    orders = run_cypher(
        "MATCH (po:ProductionOrder)-[:REQUIRES_PART|requiresPart]->"
        "(s:Specification {identifier: $sid}) "
        "RETURN DISTINCT po.identifier AS id",
        {"sid": spec_id},
    )
    for row in orders:
        oid = row.get("id")
        if oid:
            report["affected_orders"].append({"identifier": oid, "severity": "HIGH"})
            _impact_order(ca_id, oid, "HIGH", hold=True)

    # Processes via operations
    procs = run_cypher(
        "MATCH (pp:ProductionProcess)-[:hasTask|HAS_TASK]->(:OperationalTask)"
        "-[:realizesOperation|REALIZES_OPERATION]->(op:Operation)"
        "-[:REQUIRES_PART|requiresPart]->(s:Specification {identifier: $sid}) "
        "RETURN DISTINCT pp.identifier AS id",
        {"sid": spec_id},
    )
    for row in procs:
        pid = row.get("id")
        if pid:
            report["affected_processes"].append({"identifier": pid, "severity": "HIGH"})
            _impact_process(ca_id, pid, "HIGH", hold=True)


