def _traverse_operation_spec(ca_id: str, spec_id: str, report: dict):
    """OperationSpec → tasks, processes, work cells, variants, shops."""
    tasks = run_cypher(
        "MATCH (t:OperationalTask)-[:realizesOperation|REALIZES_OPERATION]->"
        "(s:Specification {identifier: $sid}) "
        "RETURN DISTINCT t.identifier AS id",
        {"sid": spec_id},
    )
    task_ids = []
    for row in tasks:
        tid = row.get("id")
        if tid:
            task_ids.append(tid)
            report["affected_tasks"].append({"identifier": tid, "severity": "HIGH"})
            _impact_task(ca_id, tid, "HIGH")

    if task_ids:
        procs = run_cypher(
            "MATCH (pp:ProductionProcess)-[:hasTask|HAS_TASK]->(t:OperationalTask) "
            "WHERE t.identifier IN $task_ids "
            "RETURN DISTINCT pp.identifier AS id",
            {"task_ids": task_ids},
        )
        for row in procs:
            pid = row.get("id")
            if pid:
                report["affected_processes"].append({"identifier": pid, "severity": "HIGH"})
                _impact_process(ca_id, pid, "HIGH")

    cells = run_cypher(
        "MATCH (wc:WorkCell)-[:PERFORMS|performs]->(s:Specification {identifier: $sid}) "
        "RETURN DISTINCT wc.identifier AS id",
        {"sid": spec_id},
    )
    for row in cells:
        cid = row.get("id")
        if cid:
            report["affected_work_cells"].append({"identifier": cid, "severity": "HIGH"})

    variants = run_cypher(
        "MATCH (vv:VehicleVariant)-[:HAS_OPERATION|hasOperation]->"
        "(s:Specification {identifier: $sid}) "
        "RETURN DISTINCT vv.identifier AS id",
        {"sid": spec_id},
    )
    for row in variants:
        vid = row.get("id")
        if vid:
            report["affected_variants"].append({"identifier": vid, "severity": "MEDIUM"})
            _impact_vehicle(ca_id, vid, "MEDIUM")

    shops = run_cypher(
        "MATCH (sh:ProductionShop)-[:MANAGES|manages]->(wc:WorkCell)"
        "-[:PERFORMS|performs]->(s:Specification {identifier: $sid}) "
        "RETURN DISTINCT sh.identifier AS id",
        {"sid": spec_id},
    )
    for row in shops:
        sid2 = row.get("id")
        if sid2:
            report["affected_shops"].append({"identifier": sid2, "severity": "MEDIUM"})


